package org.msh.pharmadex.service;

public class ManufacFP {
	
	private String productName;
	private String companyName;
	private String addr;	
	private String appShelf;
	private String storageCond;
	
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	
	public String getcompanyName() {
		return companyName;
	}
	public void setcompanyName(String companyName) {
		this.companyName = companyName;
	}
	
	public String getaddr() {
		return addr;
	}
	public void setaddr(String addr) {
		this.addr = addr;
	}
	public String getStorageCond() {
		return storageCond;
	}
	public void setStorageCond(String storageCond) {
		this.storageCond = storageCond;
	}
	public String getAppShelf() {
		return appShelf;
	}
	public void setAppShelf(String appShelf) {
		this.appShelf = appShelf;
	}
	
	

}
